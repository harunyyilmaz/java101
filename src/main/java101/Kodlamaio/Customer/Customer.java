package main.java101.Kodlamaio.Customer;

public class Customer {

    private int id;
    private String customerNummer;
    private String phone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCustomerNummer() {
        return customerNummer;
    }

    public void setCustomerNummer(String customerNummer) {
        this.customerNummer = customerNummer;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
